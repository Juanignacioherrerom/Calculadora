
package modulo;

public class Calculo {
    int a;
    int b;

    public Calculo(int a, int b) {
        this.a = a;
        this.b = b;
    }
    
    public int Sumar() {
    return a+b;
    }
    
    public int Restar() {
    return a-b;
    }
    
    public int Multiplicar(){
    return a*b;
    }
        
}
